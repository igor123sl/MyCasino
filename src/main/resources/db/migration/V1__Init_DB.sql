CREATE TABLE IF NOT EXISTS casino_player(
    id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY NOT NULL UNIQUE,
    name VARCHAR UNIQUE NOT NULL,
    email VARCHAR UNIQUE NOT NULL,
    password VARCHAR NOT NULL,
    balance INT CHECK ( balance >= 0 ) DEFAULT 0,
    role VARCHAR NOT NULL,
    is_account_non_locked BOOLEAN NOT NULL
);

CREATE TABLE IF NOT EXISTS roulette_bet(
    id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY NOT NULL UNIQUE,
    player_id INT NOT NULL,
    volume INT NOT NULL,
    bet_color VARCHAR NOT NULL,
    FOREIGN KEY (player_id) REFERENCES casino_player (id) ON DELETE CASCADE
);